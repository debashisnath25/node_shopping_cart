                  function doFilter() {
                    var string = getFilterUrl(getUrlParam(location.search));
                    if (string && typeof loadCatalogPage == 'function') {
                      loadCatalogPage(location.pathname + '?' + string);
                    }
                    else if (string) {
                      location = location.pathname + '?' + string;
                    }
